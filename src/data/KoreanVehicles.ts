// 전체 차량 데이터베이스
export const ALL_VEHICLES: VehicleSpec[] = [
  ...HYUNDAI_VEHICLES,
  ...KIA_VEHICLES,
  ...GENESIS_VEHICLES
];

// ID로 차량 검색
export function getVehicleById(id: string): VehicleSpec | undefined {
  return ALL_VEHICLES.find(v => v.id === id);
}

// 브랜드로 차량 검색
export function getVehiclesByBrand(brand: string): VehicleSpec[] {
  return ALL_VEHICLES.filter(v => v.brand === brand);
}

// 카테고리로 차량 검색
export function getVehiclesByCategory(category: VehicleSpec['category']): VehicleSpec[] {
  return ALL_VEHICLES.filter(v => v.category === category);
}

// 가격대로 차량 검색
export function getVehiclesByPriceRange(min: number, max: number): VehicleSpec[] {
  return ALL_VEHICLES.filter(v => v.price >= min && v.price <= max);
}
